<p align="justify"><font size="5">Helga Hufflepuff came from a broad valley. The Sorting Hat describes her as "good Hufflepuff" or "sweet Hufflepuff". She favoured loyalty, honesty, and dedication. In Goblet of Fire, she is said to have considered "hard workers almost always most worthy of admission". Elsewhere, she is described as taking "all the rest" of the students after selection by her colleagues. She was a good friend of Rowena Ravenclaw; their friendship is used to emphasise the failed friendship between Godric Gryffindor and Salazar Slytherin."One of the four celebrated Founders of Hogwarts, Hufflepuff was particularly famous for her dexterity at food-related Charms. Many recipes traditionally served at Hogwarts feasts originated with Hufflepuff." Her wizard card, penned by Rowling, describes her as having "brought people from different walks of life together to help build Hogwarts", and being "loved for her charming ways". </p></font></td></tr></table>
