</h3></td></tr></font>
<tr><td>
<p align="justify"><font size="5">Hufflepuff is one of the four Houses of Hogwarts School of Witchcraft and Wizardry. Its founder was the medieval witch Helga Hufflepuff. The Head of Hufflepuff is Pomona Sprout. The Fat Friar is the House's patron ghost.<br>
<br>
 Hufflepuff is the most inclusive among the four houses; valuing hard work, patience, loyalty, and fair play rather than a particular aptitude in its members. Its emblematic animal is the badger, and yellow and black are its colours.
Hufflepuff corresponds roughly to the element of earth.
</p></td></tr></font>
<tr><td><br>

<h2>THE COMMON ROOM</h2></td></tr>
<tr><td>
<p align="justify"><font size="5">The Hufflepuff Dormitories and Common room have never been seen by outsiders and is the only house with repelling devices in case of intruders. They are accessed through a pile of large barrels, found stacked in a shadowy stone recess on a right-hand side corridor near the kitchens. The barrel two from the bottom, middle of the second row, will open if tapped in the rhythm of 'Helga Hufflepuff'.<br>
<br>

As a security device to repel non-Hufflepuffs, tapping on the wrong barrel, or tapping the incorrect number of times, results in one of the other lids bursting off and drenching the imposter in vinegar.<br>
<br>

However, once you make it through the tunnel entryway, the Hufflepuff dormitory is a quite cosy and welcoming place; it always feels sunny. The common room is a large, earthy, round room with low ceilings. The view from the round windows is of dandelions and rippling grass.<br>
<br>

The decor emphasises the earthy-feel of the room with plants hanging and resting all about the room, some that even sing and dance. Burnished copper touchings with plush, cosy yellow and black patterned sofas and chairs welcoming a Hufflepuff student to relax and discuss the interesting specimens brought in by the Head of House, Professor Sprout. A Hufflepuff student retires through big, round doors in the walls of the common room to their dormitory.<br>

</p></td></tr></font>
