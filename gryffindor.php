<h2 align="center">GRYFFINDOR</h2></tr></td>
<tr><td align="center">
<h3 align="center"><font size="5">"You might belong in Gryffindor,<br>

Where dwell the brave at heart,<br>

Their daring, nerve and chivalry<br>

Set Gryffindors apart"<br>

—The Sorting Hat</h3></font>
</tr></td>
<tr><td>
<p align="justify"><font size="5">Gryffindor is one of the four Houses of Hogwarts School of Witchcraft and Wizardry, founded by Godric Gryffindor. Its emblematic animal is the lion, and its colours are scarlet and gold. Minerva McGonagall is the most recent Head of House. Sir Nicholas de Mimsy-Porpington, also known as "Nearly Headless Nick" is the house ghost. The particular characteristics of students Sorted into Gryffindor are courage and chivalry.
Gryffindor corresponds roughly to the element of fire, which may partly be the reason for its colours.
</p></tr></td></font>
<tr><td><br>

<h2 align="left">THE COMMON ROOM</h2></td></tr>
<tr><td>
<p align="justify"><font size="5">The Gryffindor Common Room is located in one of the castle's towers (Gryffindor Tower), the entrance to which is located on the seventh floor and is guarded by an oil painting of the Fat Lady, who is garbed in a pink silk dress. She permits entry only after being given the correct (regularly changing) password. Behind her is a large common room, with a fireplace, and two staircases leading up to the girls' and boys' dormitories. It is very comfortable, and members of the Gryffindor house meet there for study groups, celebrations, or relaxation.</p></td></tr></font>
<tr><td>
<br>

<h2>THE REPUTATION</h2></td></tr>
<tr><td>
<p align="justify"><font size="5">The Gryffindor house emphasises the traits of courage as well as "daring, nerve, and chivalry," and thus its members are generally regarded as brave, though sometimes to the point of recklessness. Notably, Gryffindor house contributed many members to Dumbledore's Army and the Order of the Phoenix.<br><br>

 According to Phineas Nigellus Black, members of other houses, particularly Slytherin, sometimes feel that Gryffindors engage in "pointless heroics." Another Slytherin, Severus Snape, considered many Gryffindors to be self-righteous and arrogant, with no regard for rules.<br>
<br>

However, this may be taken with a grain of salt, as Gryffindors and Slytherins have shared a fierce house rivalry since their founders, Godric Gryffindor and Salazar Slytherin, respectively, had a severe falling out over Slytherin's emphasis on blood purity. There might also be an element of feeling wronged;as Harry and his fellow Gryffindors tend to win in a lot of circumstances which, when viewed from a Slytherin's point of view, may be considered unfair. Examples of this include the last-minute points awarded by Headmaster Dumbledore at the Leaving Feast, which conveniently put Gryffindor ten points ahead of Slytherin in the 1991–1992 school year, the fact that no points were deducted for the rule breaking that happened during that night and Harry being permitted by Professor McGonagall to have his own broomstick for the Gryffindor Quidditch team, even though first-years are not normally permitted their own broomsticks.<br>
<br>

Even members of other houses might have felt that Gryffindor received favouritism, considering that many saw Harry being chosen as the fourth competitor in the Triwizard Tournament as stealing Hufflepuff student Cedric Diggory's thunder unfairly.<br>
<br>

Overall, however, the houses of Ravenclaw and Hufflepuff tended to side with Gryffindor in its rivalry with Slytherin. This was particularly notable during the Second Wizarding War — the members of Dumbledore's Army were from Gryffindor, Hufflepuff, and Ravenclaw, but lacked any Slytherins (This may be because they were not notified of the Army's creation as it came through word of mouth and they did not seem to associate with many or any Slytherins), and during the Battle of Hogwarts, the Slytherin students left while many students from the other three houses remained behind to fight against Lord Voldemort.<br></p></td></tr></font>
