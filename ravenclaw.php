<h2>THE COMMON ROOM
</h2></td></tr>
<tr><td>
<p align="justify"><font size="5">The Ravenclaw common room is in one of the castle's towers and is wide and circular. You have to climb a tight spiral staircase to get there. It has graceful arched windows, and the walls are hung with blue and bronze silks. The domed ceiling is painted with stars, which are echoed in the midnight-blue carpet. Tables, chairs, and bookcases cover the expanse of the floor, and a white-marble statue of Rowena Ravenclaw sits next to the door that leads to the dormitories above.<br><br>

 Harry Potter entered the common room in 1998 while searching for clues to the location of one of Voldemort's Horcruxes.<br>
<br>

Unlike the other common rooms in the school, a logical riddle given by a bronze eagle-shaped knocker must be answered to enter. This could prove troublesome as anyone with enough intellect could intrude into the dormitory, as is shown in Harry Potter and the Deathly Hallows where Professor McGonagall had no trouble entering by answering the riddle correctly (though she was a "hat stall" between Gryffindor and Ravenclaw). However, Hermione stated in her first year when figuring out the complicated riddle, beneath the trapdoor, that most wizards and witches do not have much sense of logic, which might be something that Ravenclaw noticed herself and decided to use clever riddles instead of passwords.
</p></font>
