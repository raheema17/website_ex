<h1>HOGWARTS SCHOOL OF WITCHCRAFT AND WIZADRY</h1><br></td></tr>
<tr><td>
<p align="justify"><font size="5">Hogwarts School of Witchcraft and Wizardry is a British wizarding boarding school teaching the magical arts located in Scotland inside Hogwarts Castle. The castle is in the mountains near a loch. The precise location can never be uncovered because the school is hidden by the most powerful spells possible, and is Unplottable. If a Muggle were to pass by the school, all they would see is a pile of rubble with a sign that says "Danger". However, according to Hermione Granger, it is "not far" from Dufftown. The motto of the school is:<b><i> "Draco dormiens nunquam titillandus"</i></b>, which is Latin for Never tickle a sleeping dragon.
There are approximately a thousand students attending Hogwarts at any given time. Upon arriving at the school, students are Sorted into four houses: Gryffindor, Hufflepuff, Ravenclaw, and Slytherin based on the qualities and aspirations of the student. Students are rewarded with points for good behavior, quidditch match victories, and correct answers to questions, and points are taken away for wrongdoings. At the end of the year, the House with the most points wins the House Cup.

Established in c. 9th/10th century, Hogwarts is considered one of the finest magical institutions in the wizarding world, though other schools include Beauxbatons Academy of Magic and the Durmstrang Institute. Children with magical abilities may be enrolled at birth and acceptance is confirmed by Owl post at age eleven.<br>

