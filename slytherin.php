<h2 align="center">SLYTHERIN</h2></td></tr>
<tr><td>
<h3 align="center"><font size="5">"Or perhaps in Slytherin,<br>

You'll make your real friends,<br>

Those cunning folk use any means,<br>

To achieve their ends."<br>

—The Sorting Hat<br>

</h3></font></td></tr>
<tr><td>
<p align="justify"><font size="5">Slytherin is one of the four Houses at Hogwarts School of Witchcraft and Wizardry, and is traditionally home to students who exhibit such traits as cunning, resourcefulness and ambition. Its emblematic animal is the snake and its colours are green and silver. The house ghost is the Bloody Baron. Founded by Salazar Slytherin, the house is composed mostly of pure-blood students, due to its founder's mistrust of Muggle-born witches and wizards. Slytherin house has a negative reputation, with many claiming that it is the source of most Dark Wizards in Britain, notably Tom Marvolo Riddle, a.k.a. Lord Voldemort, as well as many of his Death Eaters (followers). It has a traditional rivalry with Gryffindor. The great wizard Merlin was in Slytherin.
Slytherin corresponds roughly with the element of water.<br>
<br>

Slytherins tend to be ambitious, shrewd, cunning, strong leaders, and achievement-oriented. They also have highly developed senses of self-preservation.This means that Slytherins tend to hesitate before acting, so as to weigh all possible outcomes before deciding exactly what should be done.<br>
<br>

According to Albus Dumbledore, the qualities which Salazar valued in the students he chose included cleverness, resourcefulness, determination, and "a certain disregard for the rules." Dumbledore noted that all of these were qualities possessed by Harry Potter, who was in Gryffindor.<br>

</p></font></td></tr>
<tr><td><br>

<h2>THE COMMON ROOM</h2></td></tr>
<tr><td>
<p align="justify"><font size="5">The Slytherin common room is found in the dungeons of Hogwarts Castle, and is located underneath the Black Lake. Because of its location, the light in the common room is green. Harry Potter along with Ron Weasley entered the common room in his second year to find out if Draco Malfoy was the one opening the Chamber of Secrets.
</p></font></td></tr>
<tr><td>
<br>
<h2>PURE BLOOD</h2></td></tr>
<tr><td>
<p align="justify"><i><font size="5">"I always knew Salazar Slytherin was a twisted old loony,<br>
 but I never knew he started all this pure-blood stuff.<br>
 I wouldn't be in his house if you paid me. Honestly,<br>
 if the Sorting Hat had tried to put me in Slytherin,<br>
 I'd've got the train straight back home..."<br>

—Ron Weasley expressing his claims on Slytherin's views.</i></p></font></td></tr>
<tr><td>
<br>
<p align="justify"><font size="5">The Sorting Hat claims that blood purity, is a factor in selecting Slytherins. This suggests that it bases its decisions more upon the views of its members than on any accurate test of a student's lineage. Muggle-born Slytherins exist, but are very rare, as noted insultingly by Scabior the Snatcher. There have also been definite examples of half-bloods sorted into the house, including Tom Marvolo Riddle and Severus Snape.<br>
<br>
 Harry Potter was nearly sorted into Slytherin, but ended up in Gryffindor instead. Also, pure-bloods are not automatically placed in Slytherin. There have been various examples of pure-bloods sorted into other houses, including the Weasley family, Sirius Black, Neville Longbottom, and James Potter I all being sorted into Gryffindor house, Ernie Macmillan being sorted into Hufflepuff, and Morag MacDougal and Stephen Cornfoot being sorted into Ravenclaw.<br>
<br>

Contrary to the wishes of the other founders of Hogwarts School of Witchcraft and Wizardry, Salazar Slytherin wanted a stricter policy of admission to the school —one limited strictly to pure-blood students. The controversy this created apparently led to him leaving the school and the Chamber of Secrets behind. A millennium later, the house's students are still considered affiliated with Salazar's reputed views, and open expression of pro-blood purity sentiment does seem to be more common in Slytherin than in other Houses.<br>
<br>


This disagreement over blood purity, which caused Salazar Slytherin's friendship with Godric Gryffindor to break, is the origin of the feud between Slytherin and Gryffindor, the two most competitive of the houses. Slytherin and Gryffindor are generally rivals in Quidditch and for the House Cup, which the former had won seven years in a row prior to Harry Potter's arrival at Hogwarts in 1991. It is also rare for Slytherin and Gryffindor students to be friendly with one another, and common for them to share animosity. This was particularly the case during the First and Second Wizarding Wars.<br>
<br>
 During the latter conflict, no Slytherin students joined an association known as Dumbledore's Army; however, this could be explained by the fact that only those Hermione Granger, Ron Weasley, and Harry Potter trusted were invited. Some Slytherins declined to defend their school in the Battle of Hogwarts, with some joining the Death Eaters. However, there are exceptions, such as Slytherin Severus Snape and Gryffindor Lily Evans, who were best friends until Snape's use of the slur "Mudblood" and his aspirations to be a Death Eater caused Lily to sever their ties.<br>
