<p align="justify"><font size="5">Rowena Ravenclaw was a witch noted for her cleverness and creativity, and was described by Xenophilius Lovegood as beautiful. The Sorting Hat introduces her as "Fair Ravenclaw, from glen", suggesting she was from Scotland. Ravenclaw devised the ever-changing floor plans and moving staircases in the Hogwarts castle, and coined the proverb "Wit beyond measure is man's greatest treasure." Ravenclaw is described by the Sorting Hat as having selected students according to intelligence and wisdom. "One of the four famous Founders of Hogwarts School of Witchcraft and Wizardry, Rowena Ravenclaw was the most brilliant witch of her time, though legend has it that a broken heart—cause unknown—contributed to her early demise." It is revealed in the Deathly Hallows that the broken heart contributing to her untimely death was most likely the loss of her daughter, Helena Ravenclaw, who is, in fact, the Ravenclaw House Ghost (nicknamed The Grey Lady), and the loss of her diadem, the very relic to which Ravenclaw's astounding wisdom was attributed.</font></p></td></tr>
