<p align="justify"><font size="5">Salazar Slytherin is described as power hungry by the Sorting Hat, and was known as "shrewd Slytherin from fen". One of the four celebrated Founders of Hogwarts School of Witchcraft and Wizardry, Salazar Slytherin was one of the first recorded Parselmouths, an accomplished Legilimens, and a notorious champion of pureblood supremacy." Slytherin is the only founder whose physical appearance is ever described in any detail; his statue in the Chamber of Secrets depicts a man "ancient and monkey-like, with a long thin beard that fell almost to the bottom of his sweeping robes." According to Dumbledore, the qualities that Slytherin prized in his handpicked students included his own rare ability to speak Parseltongue, resourcefulness, and determination. He also selected his students according to cunning, ambition, and blood purity.</font></p></td></tr></table>
